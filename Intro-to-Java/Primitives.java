public class Primitives {
    // Primitives are datatypes that cannot be broken into any other datatype
    public static void main(String[] args) {
        int rollno = 64;  // 4 bytes, default type for integers
        long largeInteger = 45626293456L;  // 8 bytes

        // String is not primitive
        String name = "Ancil";

        char letter = 'A';
        float marks = 95.43f;  // 4 bytes
        double largeDecimalNumber = 485773.2358;  // 8 bytes, default type for decimal numbers
        boolean flag = true;
    }
}
